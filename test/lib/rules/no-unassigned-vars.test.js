import {
    describe,
    assertEqual,
    assertNonEmptyString,
} from "../../deps.js";

import { lintText } from "../../../mod.js";

const valid = [
    { text: "let x;" },
    { text: "var x;" },
    { text: "const x = undefined; log(x);" },
    { text: "let y = undefined; log(y);" },
    { text: "var y = undefined; log(y);" },
    { text: "let a = x, b = y; log(a, b);" },
    { text: "var a = x, b = y; log(a, b);" },
    { text: "const foo = (two) => { let one; if (one !== two) one = two; }" },
];

const invalid = [
    {
        text: "let x; let a = x, b; log(x, a, b);",
    },
    {
        text: "const foo = (two) => { let one; if (one === two) {} }",
    },
    {
        text: "let user; greet(user);",
    },
    {
        text: "function test() { let error; return error || 'Unknown error'; }",
    },
    {
        text: "let options; const { debug } = options || {};",
    },
    {
        text: "let flag; while (!flag) { }",
    },
    {
        text: "let config; function init() { return config?.enabled; }",
    },
];

describe("no-unassigned-vars", ({ describe }) => {

    const globalRules = { "no-unassigned-vars": ["error"] };

    describe("valid code", ({ it }) => {
        it("has expected outcomes", () => {
            valid.forEach(({ text, code, options, languageOptions }, i) => {
                const sourceText = text ?? code;
                const file = { text: sourceText };

                let rules = globalRules;
                if (options) {
                    rules = structuredClone(globalRules);
                    rules["no-unassigned-vars"] = rules["no-unassigned-vars"].concat(options);
                }

                const res = lintText(file, rules, languageOptions);

                if (res.errorCount > 0 || res.warningCount > 0) {
                    // eslint-disable-next-line no-console
                    console.error(res);
                }

                assertEqual(0, res.errorCount, `errorCount:[${i}]:${sourceText.slice(0, 52)} ...`);
                assertEqual(0, res.warningCount, `warningCount:[${i}]:${sourceText.slice(0, 52)} ...`);
            });
        });
    });

    describe("invalid code", ({ it }) => {
        it("has expected outcomes", () => {
            invalid.forEach(({ text, code, options, languageOptions, errors }, i) => {
                const sourceText = text ?? code;
                const file = { text: sourceText };

                let rules = globalRules;
                if (options) {
                    rules = structuredClone(globalRules);
                    rules["no-unassigned-vars"] = rules["no-unassigned-vars"].concat(options);
                }

                const res = lintText(file, rules, languageOptions);

                if (errors === undefined) {
                    assertEqual(true, res.errorCount > 0, `errorCount:[${i}]:${sourceText.slice(0, 52)} ...`);
                } else {
                    assertEqual(errors, res.errorCount, `errorCount:[${i}]:${sourceText.slice(0, 52)} ...`);
                }
                assertEqual(0, res.warningCount, `warningCount:[${i}]:${sourceText.slice(0, 52)} ...`);

                res.messages.forEach((message) => {
                    assertEqual("no-unassigned-vars", message.ruleId, `message.ruleId:[${i}]:${sourceText.slice(0, 52)} ...`);
                    assertNonEmptyString(message.message, `message.message:[${i}]:${sourceText.slice(0, 52)} ...`);
                });
            });
        });
    });
});
